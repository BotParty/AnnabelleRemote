package com.lukeyes.annabelleremote;

public class Favorite {

    public Favorite() {}

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    private String label;
    private String text;
}
